document.addEventListener('keydown', function(event) {
    if (event.key === "1") {
        window.location.href = '../index.html';
    } else if (event.key === "2") {
        window.location.href = './work/discord-clone.html';
    } else if (event.key === "3") {
        window.location.href = '../stack.html'; 
    }
});
